package L_001_basictype;

public class division {

	public static void main(String[] args) {
		int firstValue = 100;
		int secondValue = 200;
		float thirdValue = 300.3f;
		float fourthValue = 400.4f;
		double fifthValue = 500.5f;
		byte sixthValue = 123;
		short seventhValue = 2000;

		double result;
		result = firstValue / secondValue / thirdValue / fourthValue / fifthValue / sixthValue / seventhValue;
		
		System.out.println("The result is : " + result);
	}

}
