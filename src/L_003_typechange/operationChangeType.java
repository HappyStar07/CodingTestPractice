package L_003_typechange;

public class operationChangeType {

	public static void main(String[] args) {
		byte firstValue = 10;
		byte secondValue = 127;
		int result = firstValue + secondValue;
		System.out.println(result);
		
		char thirdValue = 'C';
		char fourthValue = 2;
		int result1 = thirdValue + fourthValue;
		System.out.println(result1);
		
		int fifthValue = 50;
		int sixthValue = fifthValue/3;
		System.out.println(sixthValue);
		
		float seventhValue = 50;
		float eightValue = 3;
		float result2 = seventhValue/eightValue;
		System.out.println(result2);
		
	}

}
